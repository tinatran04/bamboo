#include "Calculator.h"

int Calculator::Calculate(int x, char oper, int y)
{
    switch (oper)
    {
    case '+':
        return x + y;
    case '-':
        return x - y;
    case '*':
        return x * y;
    case '/':
        return x / y;
    case '%':
        return x % y;
    default:
        return 0.0;
    }
}